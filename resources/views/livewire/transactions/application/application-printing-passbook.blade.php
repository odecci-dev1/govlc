                if (!isNaN(pageNumber) && pageNumber >= 1 && pageNumber <= pages.length) {
                    
                    // * Scroll to the selected page
                    pages[pageNumber - 1].scrollIntoView({ behavior: 'smooth' })
                }
            });
        }


